DROP PROCEDURE MCLISOLIB.SEL04_CORONAREPORT_MC600M_RESULT_OCS_UPDATE_SP;

CREATE OR REPLACE PROCEDURE MCLISOLIB.SEL04_CORONAREPORT_MC600M_RESULT_OCS_UPDATE_SP
(
	  IN I_COR 		VARCHAR(3) 		-- COR
	, IN DAT		VARCHAR(10)	-- 접수일자
	, IN JNO		VARCHAR(5)		-- 접수번호
)
BEGIN
	UPDATE MCLISDLIB.MC600M@
	SET
		F600C15 = 'H'
	WHERE
		F600COR = I_COR
		AND F600DAT = REPLACE(DAT,'-','')
		AND F600JNO = JNO;
END