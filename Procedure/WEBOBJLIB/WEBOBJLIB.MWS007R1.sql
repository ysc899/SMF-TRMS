DROP PROCEDURE WEBOBJLIB.MWS007R1;

CREATE PROCEDURE WEBOBJLIB.MWS007R1
(
	  IN I_COR 		VARCHAR(3) 	-- COR
	, IN I_UID 		VARCHAR(12) -- 사용자 ID
	, IN I_IP	  	VARCHAR(30)	-- 로그인 IP
	, OUT O_MSGCOD	CHAR(3)		-- 메세지 코드
	, OUT O_ERRCOD	CHAR(10)	-- 에러 코드
	, IN I_MCD 	VARCHAR(20) -- 사번
)
LANGUAGE SQL
DYNAMIC RESULT SETS 1
BEGIN	
	-- Quick Setup  조회
   DECLARE CUR1  CURSOR WITH RETURN FOR  
	SELECT 
		  TRIM(S007COR	 ) AS S007COR   
		, TRIM(S007MCD) AS  S007MCD 
		, TRIM(S007RCD	) AS  S007RCD  
		, TRIM(S007RNM) AS  S007RNM 
		, TRIM(S007RCV) AS  S007RCV 
	FROM WEBDBLIB.MWS007M@
	WHERE S007COR = I_COR
	AND S007MCD = I_MCD
	ORDER BY S007COR,S007MCD,S007RCD,S007RCV ;
			
	OPEN CUR1;
	SET O_MSGCOD = '200';

END



