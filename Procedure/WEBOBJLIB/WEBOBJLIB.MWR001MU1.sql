DROP PROCEDURE WEBOBJLIB.MWR001MU1

CREATE PROCEDURE WEBOBJLIB.MWR001MU1 (IN I_COR VARCHAR(3)
									  , IN I_UID VARCHAR(12)
									  , IN I_IP  VARCHAR(30)
									  , OUT O_MSGCOD VARCHAR(3)
									  , OUT O_ERRCOD VARCHAR(10)
									  , IN I_DAT  DECIMAL(10,0)
									  , IN I_JNO  DECIMAL(7,0)
									  , IN I_R001PGY  VARCHAR(50)
									  , IN I_R001TRM  VARCHAR(50)
									  , IN I_R001WRD  VARCHAR(50)
									  , IN I_R001CLT  DECIMAL(10,0)
									  , IN I_R001GDT  DECIMAL(10,0)
									  , IN I_R001GKD  VARCHAR(1000)
									  , IN I_R001OPN  VARCHAR(300)
									  , IN I_R001STS  VARCHAR(20)
									  )
BEGIN
	--추가의뢰 기타항목 수정
		UPDATE WEBDBLIB . "MWR001M@"
		SET R001PGY = I_R001PGY
		, R001TRM = I_R001TRM
		, R001WRD = I_R001WRD
		, R001CLT = I_R001CLT
		, R001GDT = I_R001GDT
		, R001GKD = I_R001GKD
		, R001OPN = I_R001OPN
		, R001STS = I_R001STS
		, R001UUR = I_UID
		, R001UDT = TO_CHAR(CURRENT_TIMESTAMP, 'YYYYMMDD')
		, R001UTM = TO_CHAR(CURRENT_TIMESTAMP, 'hh24miss')
		, R001UIP = I_IP
		WHERE R001COR = I_COR
		AND R001DAT = I_DAT
		AND R001JNO = I_JNO ;
	SET O_ERRCOD = '';
	SET O_MSGCOD = 200 ;
END